# make cup of coffee
# 1 x cup, 1 x spoon, 1 x coffee, 200 ml water. Optional: sugar, milk, spices

# 1. Take one cup
# 2. Prepare 200 ml of hot water
# 3. Take 1 spoon instant coffee and add 10ml of the water
# 4. Stir until the instant coffee mix and become creamy
# 5. Optionally you can add sugar on this step and continue mixing
# 6. Add the rest of the water.
# 7. Optionally add some milk and finish with canella/mix spices on top
# 8. Enjoy!
